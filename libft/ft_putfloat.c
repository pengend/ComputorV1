#include "libft.h"

void ft_putfloat(double n, int c)
{
	char *tmp;

	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	tmp = ft_ditoa(n, c);
	ft_putstr(tmp);
}
